package edu.lawrence.quiz_server;

public class Quiz {
    private int id;
    private String title;
    
    
    
    public Quiz(){}

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setTitle(String title) {
        this.title = title;
    }
    
    
    
}
